// ─── MAIN ──────────────────────────────────────────────────────────────────────

async function run() {
  await mongoose.connect(MONGO_URI);
  console.log('✅ Connected to MongoDB\n');

  const Category = require('../models/Category');
  const SubCategory = require('../models/SubCategory');

  let created = 0, updated = 0, skipped = 0;

  for (const [catName, subcats] of Object.entries(DATA)) {
    // Find category — try exact name first, then partial
    let cat = await Category.findOne({ name: catName });
    if (!cat) {
      // Try case-insensitive
      cat = await Category.findOne({ name: { $regex: new RegExp('^' + catName + '$', 'i') } });
    }
    if (!cat) {
      // For 'Cider & Perry' try 'Cider' etc.
      const nameVariants = [catName.split(' ')[0], catName.replace(' & ', ' and ')];
      for (const v of nameVariants) {
        cat = await Category.findOne({ name: { $regex: new RegExp(v, 'i') } });
        if (cat) break;
      }
    }

    if (!cat) {
      console.log(`⚠️  Category not found: "${catName}" — skipping`);
      continue;
    }

    console.log(`\n📂 Category: "${cat.name}" (${cat._id})`);

    for (const [i, sub] of subcats.entries()) {
      const baseSlug = slugify(sub.name);
      // Make slug unique per parent by appending category type
      let slug = baseSlug;

      // Check if slug exists with a different parent
      const existingOther = await SubCategory.findOne({ slug, parent: { $ne: cat._id } });
      if (existingOther) {
        slug = `${baseSlug}-${slugify(cat.type || cat.name)}`;
      }

      const doc = {
        name: sub.name,
        slug,
        parent: cat._id,
        parentPath: cat.slug,
        type: sub.type,
        description: sub.description,
        shortDescription: sub.shortDescription || '',
        tagline: sub.tagline || '',
        icon: sub.icon || '',
        color: sub.color || '#6B7280',
        characteristics: sub.characteristics || {},
        typicalFlavors: sub.typicalFlavors || [],
        commonPairings: sub.commonPairings || [],
        seasonal: {
          spring: !!(sub.seasonal && sub.seasonal.spring),
          summer: !!(sub.seasonal && sub.seasonal.summer),
          fall: !!(sub.seasonal && sub.seasonal.fall),
          winter: !!(sub.seasonal && sub.seasonal.winter),
        },
        status: 'published',
        isFeatured: sub.isFeatured || false,
        displayOrder: sub.displayOrder || i + 1,
        showInMenu: true,
        metaTitle: `${sub.name} | DrinksHarbour`,
        metaDescription: sub.shortDescription || sub.description?.substring(0, 160) || '',
        metaKeywords: sub.typicalFlavors || [],
      };

      // Upsert: find by slug OR by (parent + name)
      const existing = await SubCategory.findOne({
        $or: [{ slug }, { parent: cat._id, name: sub.name }],
      });

      if (existing) {
        // Update to connect if not already connected to this parent
        if (String(existing.parent) !== String(cat._id)) {
          await SubCategory.updateOne({ _id: existing._id }, { $set: { parent: cat._id, parentPath: cat.slug, status: 'published' } });
          console.log(`  🔗 Connected existing "${sub.name}" → ${cat.name}`);
          updated++;
        } else {
          // Update all fields to keep in sync
          await SubCategory.updateOne({ _id: existing._id }, { $set: doc });
          console.log(`  ↻  Updated "${sub.name}"`);
          updated++;
        }
      } else {
        await SubCategory.create(doc);
        console.log(`  ✓  Created "${sub.name}"`);
        created++;
      }
    }
  }

  console.log(`\n${'─'.repeat(60)}`);
  console.log(`✅ Done — Created: ${created} | Updated: ${updated} | Skipped: ${skipped}`);
  await mongoose.disconnect();
}

run().catch(err => {
  console.error('❌ Error:', err.message);
  process.exit(1);
});
